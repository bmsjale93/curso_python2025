# Uso de for para Iterar sobre Secuencias
# El bucle for en Python se utiliza para recorrer elementos de una secuencia(listas, tuplas, cadenas, diccionarios y rangos). Es útil cuando se necesita repetir una tarea un número determinado de veces.

# EJEMPLO
frutas = ["manzana", "pera", "naranja"]
for fruta in frutas:
    print(fruta)

