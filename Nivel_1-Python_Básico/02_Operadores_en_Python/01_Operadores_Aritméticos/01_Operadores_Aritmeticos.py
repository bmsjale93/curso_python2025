# OPERADORES ARITMÉTICOS
# Los operadores aritméticos en Python permiten realizar operaciones matemáticas 
# como suma, resta, multiplicación, división y otras más avanzadas.

a = 10
b = 3

print("Suma: ", a + b)                  # 10 + 3 = 13
print("Resta: ", a - b)                 # 10 - 3 = 7
print("Multiplicación: ", a * b)        # 10 * 3 = 30
print("División: ", a / b)              # 10 / 3 = 3.3333
print("División Entera: ", a // b)       # 10 // 3 = 3
print("Módulo: ", a % b)                # 10 % 3 = 1
print("Potencia: ", a ** b)              # 10 ** 3 = 1000