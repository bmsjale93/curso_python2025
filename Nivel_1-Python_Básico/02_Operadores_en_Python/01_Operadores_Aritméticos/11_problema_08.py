# Calcula el perímetro de un círculo con radio r = 4, usando la fórmula:
# perímetro = 2 * π * r

# Importamos la libreria math
import math

# Obtenemos el valor de PI
PI = math.pi
print("El valor de pi es: ", PI)

# Calculamos el perímetro
r = 4
perimetro = 2 * PI * r

# Imprimimos el resultado
print(f"El resultado del perímetro es {perimetro:.2f}")
