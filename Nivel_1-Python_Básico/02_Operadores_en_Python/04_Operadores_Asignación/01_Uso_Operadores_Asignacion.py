# Uso de los Operadores de Asignación
# Los operadores de asignación permiten modificar valores sin necesidad 
# de escribir la operación completa.

# EJEMPLO 1 - Asignación Simple
x = 10      # Asignamos el valor 10 a x
print(x)    # Salida: 10

# EJEMPLO 2 - Asignación Combinada
x = 5

x += 3      # x = x + 3 -> x = 8
print(x)    # Salida: 8

x *= 2      # x = x * 2 -> x = 16
print(x)    # Salida: 16

x //= 3     # x = x // 3 -> x = 5
print(x)    # Salida: 5
