# Crea un programa que valide si una contraseña ingresada es igual a "secreto123".

# Pedimos una contraseña al usuario
contraseña = input("Por favor, introduce una contraseña: ")

# Establecemos la condición:
if contraseña == "secreto123":
    print("La contraseña es correcta.")
else:
    print("Contraseña errónea.")