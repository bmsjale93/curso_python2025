# Dado un usuario con las variables registrado = True y saldo > 0, escribe una 
# condición que permita la compra si el usuario está registrado y tiene saldo suficiente.

# Establecemos las variables
registrado = True
saldo = 1200

# Creamos la condición
if registrado and saldo > 0:
    print("Puede comprar.")
else:
    print("No puede comprar.")
