# Escribe un programa que reciba un número flotante y lo convierta en un entero.

# Solicitamos un número flotante
numero_flotante = float(input("Ingresa un número con decimales: "))

# Convertimos el número flotante
numero_entero = int(numero_flotante)

# Mostramos los valores antes y después de la conversión
print("Número ingresado (flotante): ", numero_flotante)
print("Número convertido a entero: ", numero_entero)