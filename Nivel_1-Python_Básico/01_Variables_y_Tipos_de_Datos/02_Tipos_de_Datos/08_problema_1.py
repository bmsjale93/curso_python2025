# Declara una variable para cada tipo de dato: entero, flotante, cadena, 
# booleano, lista, tupla, diccionario y conjunto. 
# Luego, imprime el tipo de cada variable.

# Declaramos las variables
numero = 20         # Entero
print(type(numero))

flotante = 19.99    # Flotante
print(type(flotante))

cadena = "Hola Mundo"   # Cadena
print(type(cadena))

booleano = True     # Booleano
print(type(booleano))

lista = ["Rojo", "Amarillo", "Naranja"] # Lista
print(type(lista))

tupla = (15.8, 25.4)    # Tupla
print(type(tupla))

diccionario = {         # Diccionario
    "nombre" : "Alex",
    "edad" : "30",
    "profesion" : "Ingeniero"
}

print(type(diccionario))

conjunto = {1, 2, 3, 4, 5}
print(type(conjunto))